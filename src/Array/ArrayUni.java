package Array;

public class ArrayUni {
    public static void main(String[] args) {
        //Crear array unidimensional
        String[] array = {"Pedro", "Jorge", "Marta", "Merche", "Paula", "Pablo", "Karina"};

        //recorrer el array unidimensional
        for (String valor: array) {
            System.out.println(valor);
        }
    }
}
